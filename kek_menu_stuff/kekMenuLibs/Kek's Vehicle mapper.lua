function vehicle_mapper.get_dimensions(hash)
	if vehicle_properties[hash] then
		local min_dim <const> = vehicle_properties[hash].min_dim 
		local max_dim <const> = vehicle_properties[hash].max_dim
		return v3(min_dim.x, min_dim.y, min_dim.z), v3(max_dim.x, max_dim.y, max_dim.z)
	else
		return v3(), v3()
	end
end

function vehicle_mapper.GetModelFromHash(hash)
	return vehicle_properties[hash] and vehicle_properties[hash].model or lang["Unknown vehicle name"]
end

function vehicle_mapper.get_english_name_regardless_of_game_language(hash)
	return vehicle_properties[hash] and vehicle_properties[hash].name or lang["Unknown vehicle name"]
end

function vehicle_mapper.get_vehicle_name(hash)
	return streaming.get_vehicle_model_name(hash) or lang["Unknown vehicle name"]
end

do
	local hashes <const> = vehicle.get_all_vehicle_model_hashes()
	function vehicle_mapper.get_random_vehicle()
		return hashes[math.random(1, #hashes)]
	end
end

function vehicle_mapper.get_hash_from_user_input(...)
	local user_input = ...
	if user_input == "?" then
		return vehicle_mapper.get_random_vehicle()
	end
	user_input = user_input:lower()
	if vehicle_properties[gameplay.get_hash_key(user_input)] then
		return gameplay.get_hash_key(user_input)
	end
	user_input = (user_input:gsub("%s", "")):gsub("ii", "2")
	for hash, properties in pairs(vehicle_properties) do

		local found_common_misspelling
		if properties.common_misspellings then
			for i = 1, #properties.common_misspellings do
				 if properties.common_misspellings[i]:gsub("%s", ""):find(user_input, 1, true) then
				 	found_common_misspelling = true
				 	break
				 end
			end
		end

		if found_common_misspelling
		or properties.model:find(user_input, 1, true) 
		or ((properties.name:lower()):gsub("%s", "")):find(user_input, 1, true) then
			return hash
		end
	end
	return 0
end

